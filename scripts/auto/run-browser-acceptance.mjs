import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

import { runBrowserAcceptance } from './browser-acceptance-core.mjs';
import {
  buildPlanExecutionEnhancement,
  createConfigDrivenScenarios,
  loadAutomationPlan
} from './browser-config-driven.mjs';
import { appendBrowserIssues } from './browser-issue-log.mjs';
import {
  createExecutableScenarios,
  plannedScenarioBacklog
} from './browser-acceptance-scenarios.mjs';

const __filename = fileURLToPath(import.meta.url);

function parseArgs(argv) {
  const args = {
    dryRun: false,
    noAppendIssues: false,
    scopes: undefined,
    failScopes: undefined,
    plan: '',
    help: false
  };

  for (const arg of argv) {
    if (arg === '--help' || arg === '-h') {
      args.help = true;
      continue;
    }
    if (arg === '--dry-run') {
      args.dryRun = true;
      continue;
    }
    if (arg === '--no-append-issues') {
      args.noAppendIssues = true;
      continue;
    }
    if (arg.startsWith('--scopes=')) {
      args.scopes = arg
        .slice('--scopes='.length)
        .split(',')
        .map((item) => item.trim())
        .filter(Boolean);
      continue;
    }
    if (arg.startsWith('--fail-scopes=')) {
      args.failScopes = arg
        .slice('--fail-scopes='.length)
        .split(',')
        .map((item) => item.trim())
        .filter(Boolean);
      continue;
    }
    if (arg.startsWith('--plan=')) {
      args.plan = arg.slice('--plan='.length).trim();
      continue;
    }
    throw new Error(`Unknown argument: ${arg}`);
  }

  return args;
}

function printHelp() {
  process.stdout.write(
    [
      'Usage:',
      '  node scripts/auto/run-browser-acceptance.mjs [--dry-run] [--no-append-issues] [--plan=config/automation/sample-web-smoke-plan.json] [--scopes=delivery,baseline] [--fail-scopes=delivery]',
      '',
      'Options:',
      '  --dry-run           Print the active scenario plan without opening a browser.',
      '  --no-append-issues  Do not append this run to docs/22-automation-test-issues-20260316.md.',
      '  --plan=...          Load a config-driven scenario plan JSON generated by the automation center.',
      '  --scopes=...        Comma-separated scenario scopes to execute. Default: delivery,baseline.',
      '  --fail-scopes=...   Comma-separated scopes that should set a non-zero exit code. Default: delivery.'
    ].join('\n')
  );
}

export async function runCli(argv = process.argv.slice(2)) {
  const args = parseArgs(argv);
  if (args.help) {
    printHelp();
    return {
      dryRun: true,
      exitCode: 0
    };
  }

  const workspaceRoot = path.resolve(path.dirname(__filename), '..', '..');
  const planInfo = args.plan ? await loadAutomationPlan(workspaceRoot, args.plan) : null;
  const target = planInfo?.plan?.target || {};

  const result = await runBrowserAcceptance({
    createScenarios: planInfo ? createConfigDrivenScenarios(planInfo.plan) : createExecutableScenarios,
    plannedScenarios: planInfo ? [] : plannedScenarioBacklog,
    options: {
      dryRun: args.dryRun,
      appendIssues: !args.noAppendIssues && !args.dryRun,
      scenarioScopes: args.scopes || target.scenarioScopes,
      failScopes: args.failScopes || target.failScopes,
      frontendBaseUrl: target.frontendBaseUrl,
      backendBaseUrl: target.backendBaseUrl,
      browserPath: target.browserPath || undefined,
      headless: target.headless,
      username: target.username,
      password: target.password,
      issueDocPath: target.issueDocPath ? path.resolve(workspaceRoot, target.issueDocPath) : undefined,
      outputPrefix: target.outputPrefix,
      enhanceResult: planInfo ? buildPlanExecutionEnhancement(planInfo) : undefined
    }
  });

  if (result.dryRun) {
    process.stdout.write(
      `${JSON.stringify(
        {
          dryRun: true,
          plan: planInfo?.absolutePath,
          scopes: result.options.scenarioScopes,
          scenarios: result.executableScenarios,
          plannedScenarios: result.plannedScenarios
        },
        null,
        2
      )}\n`
    );
    return {
      ...result,
      exitCode: 0
    };
  }

  if (result.options.appendIssues) {
    await appendBrowserIssues({
      issueDocPath: result.options.issueDocPath,
      summary: result.summary,
      scenarioResults: result.scenarioResults,
      commandHint: result.options.commandHint,
      workspaceRoot: result.options.workspaceRoot
    });
  }

  return result;
}

if (path.resolve(process.argv[1] || '') === path.resolve(__filename)) {
  try {
    const result = await runCli();
    process.exitCode = result.exitCode || 0;
  } catch (error) {
    process.stderr.write(`${error instanceof Error ? error.stack || error.message : String(error)}\n`);
    process.exitCode = 1;
  }
}
